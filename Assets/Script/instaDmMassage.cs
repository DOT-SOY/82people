    public string GetMessageByID(string itemId)
    {
        var items = DM.Where(dm => dm.ItemID == itemId).ToList();
        if (items.Count > 0)
        {
            int randomIndex = Random.Range(0, items.Count);
            return items[randomIndex].Massage;
        }
        else
        {
            Debug.LogWarning("DM item with ItemID " + itemId + " not found.");
            return null;
        }
    }


    // 특정 massageID를 가지고 있는 DMItem의 itemId를 찾는 메서드
    public string GetItemIDByMassageID(string massageID)
    {
        var item = DM.FirstOrDefault(dm => dm.MassageID == massageID);
        return item.ItemID;
    }
